<h3>What does it do?</h3>

<p>Route Risks analyses the safety of roads by using <a href="http://data.gov.uk/dataset/road-accidents-safety-data" target="_blank"><span class="glyphicon glyphicon-new-window"></span> road safety data</a> published by the UK Department for Transport.</p>

<p>To use the application, enter a start and end address for a route of your choice. You can filter collisions included in the results by selecting different severity levels and years using the checkboxes.</p>

<p>The following statistics are calculated to help assess how dangerous the route is:</p>

<ul>
    <li>
        Total number of collisions.
    </li>
    <li>
        Number of collisions per kilometre.
    </li>
    <li>
        Weighted number of collisions per kilometre. This is calculated by assigning a weighted value to each collision on the route using its severity. Fatal collisions are assigned a weight of 3, serious collisions are assigned a weight of 2, and slight collisions have a weight of 1.
    </li>
    <li>
        Total number of casualties.
    </li>
    <li>
        Number of casualties per kilometre.
    </li>
</ul>

<p>The route is displayed on an interactive map, and a heatmap is used to highlight collision hotspots. Sections of the route with the highest relative density of collisions will appear red. The heatmap also takes into account the severity of collisions, with fatal/serious collisions affecting the results more than slight collisions. </p>

<p><strong>Disclaimer:</strong> There is no guarantee that results produced by the application will be 100% accurate. In some cases, nearby collisions may be incorrectly included in results (for example, collisions on the opposite lane of a motorway). </p>

